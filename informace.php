<div class="stredovy">
Přibližně dvakrát do měsíce jsou společné akce celého oddílu, výpravy. Tyto akce jsou zaměřené hlavně na pobyt v přírodě a vše, co s tím souvisí. 
<br><br>
Vyvrcholením celoroční oddílové činnosti je potom třítýdenní tábor na začátku července. Již přes dvacet let jezdíme na Kovářovu louku nedaleko obce Soběnov u Kaplice v Novohradských horách. Tábor je takovou třešničkou na dortu celoročního programu. Děti na něm zažijí spoustu nových dobrodružství a učí se také životu v přírodě. Každé dva roky je pro rodiče uspořádán návštěvní den, kdy se mohou přijet za dětmi podívat a prožít s nimi táborový den, prohlédnout si tábořiště a zahrát si společně různé hry.
<br><br>
